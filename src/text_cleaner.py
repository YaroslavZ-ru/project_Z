"""Модуль очистки текста для AI-Terminator.

Предоставляет функцию clean_text для предварительной обработки текста.
"""

import re


def clean_text(text: str) -> str:
    """Очистить текст от ненужных символов.

    Оставляет только:
    - буквы (русские а-яё, латинские a-z)
    - цифры 0-9
    - пробелы
    - дефисы - (но не в начале и не в конце слова)

    Args:
        text: Исходный текст.

    Returns:
        Очищенный текст в нижнем регистре без лишних пробелов и краевых дефисов.
        Пустая строка, если результат пуст.
    """
    # Заменяем нежелательные символы на пробел (чтобы не слипались слова)
    cleaned = re.sub(r"[^а-яёa-z0-9\s-]", " ", text, flags=re.IGNORECASE)

    # Приводим к нижнему регистру
    cleaned = cleaned.lower()

    # Убираем лишние пробелы (замена на один пробел)
    cleaned = re.sub(r"\s+", " ", cleaned).strip()

    # Удаляем дефисы по краям
    cleaned = cleaned.strip("-")

    # Заменяем все оставшиеся дефисы на пробелы (для составных слов)
    cleaned = cleaned.replace("-", " ")

    # Схлопываем пробелы и обрезаем края
    cleaned = re.sub(r"\s+", " ", cleaned).strip()

    return cleaned
